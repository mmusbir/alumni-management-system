ALTER TABLE alumni
  ADD COLUMN IF NOT EXISTS is_verified SMALLINT NOT NULL DEFAULT 1,
  ADD COLUMN IF NOT EXISTS verified_at TIMESTAMPTZ;

ALTER TABLE usaha
  ADD COLUMN IF NOT EXISTS phone_usaha VARCHAR(20),
  ADD COLUMN IF NOT EXISTS is_verified SMALLINT NOT NULL DEFAULT 1,
  ADD COLUMN IF NOT EXISTS verified_at TIMESTAMPTZ;

UPDATE usaha u
SET phone_usaha = a.phone
FROM alumni a
WHERE a.id = u.pemilik_id
  AND (u.phone_usaha IS NULL OR TRIM(u.phone_usaha) = '');

